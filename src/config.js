import crypto from 'node:crypto';
import fs from 'node:fs';
import os from 'node:os';
import path from 'node:path';

export const DEFAULT_DATA_DIR = path.join(os.homedir(), '.bridge-data');
const ENV_FILE = process.env.ENV_FILE || path.join(process.env.DATA_DIR || DEFAULT_DATA_DIR, '.env');

function loadDotEnv(filePath = ENV_FILE) {
  const absolutePath = path.resolve(filePath);
  if (!fs.existsSync(absolutePath)) return;

  const content = fs.readFileSync(absolutePath, 'utf8');
  for (const rawLine of content.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;

    const index = line.indexOf('=');
    if (index === -1) continue;

    const key = line.slice(0, index).trim();
    if (!key || process.env[key] != null) continue;

    let value = line.slice(index + 1).trim();
    if ((value.startsWith('"') && value.endsWith('"')) || (value.startsWith("'") && value.endsWith("'"))) {
      value = value.slice(1, -1);
    }

    process.env[key] = value;
  }
}

function token() {
  return crypto.randomBytes(48).toString('base64url');
}

function envLine(key, value) {
  return `${key}=${String(value).replace(/\n/g, '')}`;
}

function ensureDotEnv(filePath = ENV_FILE) {
  const absolutePath = path.resolve(filePath);
  const generated = [];
  const defaults = {
    HOST: '127.0.0.1',
    PORT: '8080',
    API_TOKEN: token(),
    BRIDGE_TOKEN: token(),
    DATA_DIR: process.env.DATA_DIR || DEFAULT_DATA_DIR,
  };

  if (process.env.PUBLIC_BASE_URL == null) {
    defaults.PUBLIC_BASE_URL = `http://${process.env.HOST || defaults.HOST}:${process.env.PORT || defaults.PORT}`;
  }

  let existing = '';
  try {
    existing = fs.existsSync(absolutePath) ? fs.readFileSync(absolutePath, 'utf8') : '';
  } catch {
    return { path: absolutePath, generated };
  }

  const existingKeys = new Set();
  for (const rawLine of existing.split(/\r?\n/)) {
    const line = rawLine.trim();
    if (!line || line.startsWith('#')) continue;
    const index = line.indexOf('=');
    if (index > 0) existingKeys.add(line.slice(0, index).trim());
  }

  const additions = [];
  for (const [key, value] of Object.entries(defaults)) {
    if (process.env[key] != null || existingKeys.has(key)) continue;
    additions.push(envLine(key, value));
    process.env[key] = value;
    generated.push(key);
  }

  if (!additions.length) return { path: absolutePath, generated };

  try {
    fs.mkdirSync(path.dirname(absolutePath), { recursive: true });
    const prefix = existing && !existing.endsWith('\n') ? '\n' : '';
    const header = existing ? '\n# Generated by chatgpt-browser-bridge setup\n' : '# Generated by chatgpt-browser-bridge setup\n';
    fs.writeFileSync(absolutePath, `${existing}${prefix}${header}${additions.join('\n')}\n`, 'utf8');
  } catch {
    // Keep generated values in process.env even if the file cannot be written.
  }

  return { path: absolutePath, generated };
}

loadDotEnv();
export const setupInfo = ensureDotEnv();
loadDotEnv();

function intFromEnv(name, fallback) {
  const raw = process.env[name];
  if (raw == null || raw === '') return fallback;

  const value = Number.parseInt(raw, 10);
  if (!Number.isFinite(value)) {
    throw new Error(`Invalid integer env ${name}: ${raw}`);
  }

  return value;
}

function boolFromEnv(name, fallback) {
  const raw = process.env[name];
  if (raw == null || raw === '') return fallback;
  return /^(1|true|yes|on)$/i.test(raw);
}

function csvFromEnv(name, fallback) {
  const raw = process.env[name];
  const source = raw == null || raw === '' ? fallback : raw;
  return String(source)
    .split(',')
    .map((item) => item.trim())
    .filter(Boolean);
}

